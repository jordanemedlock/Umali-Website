    <h2>Tanzania</h2>
    <p>Tanzania is a beautiful country in southeastern Africa with a population of 46 million people. It is home to the tallest mountain in Africa (Mount Kilimanjaro), the Serengeti Wildlife Preserve, Lake Victoria, and the pristine beaches of Zanzibar. Tanzania's motto is "Uhuru na Umoja" - Freedom and Unity.</p>
    <p>There is currently little awareness about autism in Tanzania. Children with autism are often not diagnosed and when they are, educational and therapy services are unavailable.</p>
