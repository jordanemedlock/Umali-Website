        <h2>
            <a data-toggle="collapse" data-taget="#who-we-are" href="#who-we-are">
                Who We Are
            </a>
        </h2>
        <div class="collapse in" id="who-we-are">
            <p>
                Each child with Autism Spectrum Disorder has a journey filled with gifts and challenges. Children with ASD in Tanzania are largely undiagnosed. Many of their gifts remain hidden while the challenges can be severe. Our Mission at Umali Autism Foundation Tanzania is to bring them Special Education and Health Care Services which have hitherto been unavailable in their culture.
            </p>
            <p>
                The journey of each child with Autism Spectrum Disorder is shared by a family. Our Mission at UAFT is also to support the journey of each child's family.
                Umali Autism Foundation Tanzania (UAFT) is a group of American and Tanzanian professionals who are working together towards these goals in two very specific ways. The first is by developing a network in Tanzania of future educators and care providers for children with ASD for whom our organization can provide training and support. The second is to raise funds to support and house that network.
            </p>
        </div>
