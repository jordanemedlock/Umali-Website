        <h2>
          <a class="accordion-toggle" data-toggle="collapse" data-parent="#autism-accordion" href="#what">
            What is Autism?
          </a>
        </h2>
      </div>
      <div id="what" class="accordion-body collapse in">
        <div class="accordion-inner">
          <p>
            Autism or Autism Spectrum Disorder (<abbr class="initialism" title="Autism Spectrum Disorder">ASD</abbr>) is a disorder that is characterized by varying degrees of difficulties in the areas of social engagement, verbal and non-verbal communication, and repetitive behaviors or intense interests.  The deficits seen in ASD emerge before a child's 3rd birthday, and some children progress normally then begin to regress around 18 to 30 months of age.  People with ASD have a range of cognitive abilities; ASD can be associated with intellectual disability or normal to above average intelligence.  Differences in processing sensory information, such as sight, sound, and touch are also common in ASD.  Knowledge about the causes of autism continue to develop.  There appear to be many causes for ASD, researchers have begun to identify genetic links to autism, which may make a child genetically predisposed to develop ASD.
          </p>
        </div>
      </div>
    </div>
    <div class="accordion-group">
      <div class="accordion-heading">
        <h2>
          <a class="accordion-toggle" data-toggle="collapse" data-parent="#autism-accordion" href="#how">
            How Common is Autism?
          </a>
        </h2>
      </div>
      <div id="how" class="accordion-body collapse">
        <div class="accordion-inner">
          <p>The number of children identified with ASD is increasing every year.  The increased prevalence appears to be partially due to better screening and evaluation of children, but there are additional factors, which have led to this rapid increase, that are unknown at this time.  According to the Centers for Disease Control and Prevention:</p>
          <ul>
            <li>About 1 in 88 children has been identified with ASD in the US.</li>
            <li>ASDs are reported to occur in all racial, ethnic, and socioeconomic groups in the US.</li>
            <li>ASDs are almost 5 times more common among boys (1 in 54) than among girls (1 in 252) in the US.</li>
          </ul>
          <a href="http://www.cdc.gov/ncbddd/autism/images/autism_data_graphic2012.jpg" class="thumbnail autism"><img src="http://www.cdc.gov/ncbddd/autism/images/autism_data_graphic2012.jpg" alt="Autism Prevalence"/>From the Center for Disease Control and Prevention</a>
        </div>
      </div>
    </div>
    <div class="accordion-group">
      <div class="accordion-heading">
        <h2>
          <a class="accordion-toggle" data-toggle="collapse" data-parent="#autism-accordion" href="#autism-in-tanzania">
          Autism in Tanzania
          </a>
        </h2>
      </div>
      <div id="autism-in-tanzania" class="accordion-body collapse">
        <div class="accordion-inner">
          <p>
            Little is known about the prevalence of autism in Africa.  Some countries in African have been raising awareness about ASD and beginning to diagnose children.  However, at this time, almost nothing is known about autism in Tanzania.  Many children with autism in Tanzania are undiagnosed and there are limited resources available for support after a diagnosis is given.  There are grassroots groups, like Umali Autism Foundation Tanzania, that are beginning efforts to increase awareness, develop culturally appropriate screening and evaluation tools, provide training to educators, therapists, and medical professionals, and create the infrastructure necessary to provide medical and education services to children with autism.  Until more services are in place, it will be difficult to study the prevalence of autism in Tanzania.
          </p>
        </div>
